    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String email = jTextField2.getText();
        String password = jTextField3.getText();
        type=jComboBox1.getSelectedIndex();
        
        if(name.isEmpty()||email.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please Enter all values",
                "User", JOptionPane.WARNING_MESSAGE  );
        }else{
            try {
                Connection Conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "root");
                Statement myStat = Conn.createStatement();
                myStat.executeUpdate("INSERT INTO user"+" VALUES (NULL,'"+name+"','"+ email+"',"+type+",'"+password+"')");
                jTextField1.setText("");
                jTextField2.setText("");
                Conn.close();
                JOptionPane.showMessageDialog(this, "User Added",
                "User", JOptionPane.INFORMATION_MESSAGE  );
                Main_frame f=new Main_frame();
                f.setVisible(true);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(addbook.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error",
                    "User", JOptionPane.WARNING_MESSAGE  );
            }
            
        }

    }//GEN-LAST:event_jButton1ActionPerformed

