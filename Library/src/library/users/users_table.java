    public void fillUserTable(ArrayList<user> users) {
        this.users = users;
        //put results in a table
        DefaultTableModel model = (DefaultTableModel) usersTable.getModel();
        Object[] row = new Object[5];
        for (int i = 0; i < users.size(); i++) {
            user user = users.get(i);
            row[0] = user.getId();
            row[1] = user.getName();
            row[2] = user.getEmail();
            row[3] = user.getUserTypeName();

            model.addRow(row);
        }

    }

