    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int result = JOptionPane.showConfirmDialog(this, "Do you want to delete this user?", "Delete user",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (result == JOptionPane.YES_OPTION) {
            try {
                c = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "root");
                Statement myS = c.createStatement();
                String SQL = "delete from user where id = " + s.get(3);
                myS.execute(SQL);
                JOptionPane.showMessageDialog(this, "User Deleted",
                        "User", JOptionPane.INFORMATION_MESSAGE);
                new users_management().setVisible(true);
                this.dispose();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "this user has dependencies\n can't delete user.",
                        "User", JOptionPane.INFORMATION_MESSAGE);
            }
        } else if (result == JOptionPane.NO_OPTION) {

        }

    }//GEN-LAST:event_deleteActionPerformed

